package com.itismatt.dozer_discord.Util;


/**
 * Utility Class for managing coordinate points (x , y)
 */
@SuppressWarnings("WeakerAccess")
public class Point
{
    public double x;
    public double y;


    public Point()
    {
        // Nothing
    }


    @SuppressWarnings("unused")
    public Point(final double X , final double Y)
    {
        x = X;
        y = Y;
    }
}
